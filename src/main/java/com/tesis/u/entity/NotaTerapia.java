package com.tesis.u.entity;

import java.util.Date;

public class NotaTerapia {
	
	private String idPaciente;
	private String objetivo;
	private String estructuraCorporal;
	private String funcionCorporal;
	private String pronostico;
	private String planTrabajo;
	private Date fechaNotaTerapia;
	private String horaNotaTerapia;
	private String observacion;
	private String idTipoTerapia;
	private String idEnfermera;
	
	
	public String getIdPaciente() {
		return idPaciente;
	}
	public void setIdPaciente(String idPaciente) {
		this.idPaciente = idPaciente;
	}
	public String getObjetivo() {
		return objetivo;
	}
	public void setObjetivo(String objetivo) {
		this.objetivo = objetivo;
	}
	public String getEstructuraCorporal() {
		return estructuraCorporal;
	}
	public void setEstructuraCorporal(String estructuraCorporal) {
		this.estructuraCorporal = estructuraCorporal;
	}
	public String getFuncionCorporal() {
		return funcionCorporal;
	}
	public void setFuncionCorporal(String funcionCorporal) {
		this.funcionCorporal = funcionCorporal;
	}
	public String getPronostico() {
		return pronostico;
	}
	public void setPronostico(String pronostico) {
		this.pronostico = pronostico;
	}
	public String getPlanTrabajo() {
		return planTrabajo;
	}
	public void setPlanTrabajo(String planTrabajo) {
		this.planTrabajo = planTrabajo;
	}
	public Date getFechaNotaTerapia() {
		return fechaNotaTerapia;
	}
	public void setFechaNotaTerapia(Date fechaNotaTerapia) {
		this.fechaNotaTerapia = fechaNotaTerapia;
	}
	public String getHoraNotaTerapia() {
		return horaNotaTerapia;
	}
	public void setHoraNotaTerapia(String horaNotaTerapia) {
		this.horaNotaTerapia = horaNotaTerapia;
	}
	public String getObservacion() {
		return observacion;
	}
	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}
	public String getIdTipoTerapia() {
		return idTipoTerapia;
	}
	public void setIdTipoTerapia(String idTipoTerapia) {
		this.idTipoTerapia = idTipoTerapia;
	}
	public String getIdEnfermera() {
		return idEnfermera;
	}
	public void setIdEnfermera(String idEnfermera) {
		this.idEnfermera = idEnfermera;
	}
	
	
	

}
